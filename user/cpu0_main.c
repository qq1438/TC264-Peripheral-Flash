/*
 * 功能概述
 * - CPU0 侧负责“刷写存储”：当检测到图像存储模块的 is_compress 置位时，
 *   调用 store_compressed_image() 将内部缓冲区的数据按页写入 W25N04，并复位 is_compress。
 *
 * 与 CPU1 的协作约定
 * - CPU1 采集并压缩图像（或写入其它参数段）后，将全局标志 is_compress 置为 true；
 * - CPU0 轮询 is_compress，并在置位时执行刷写；刷写成功或失败后均需将 is_compress 清零，
 *   以避免重复写入；帧计数的维护由图像存储模块在写入成功后自增。
 *
 * 运行流程
 * - 初始化时钟与调试输出 → 等待多核同步 → 主循环中根据 is_compress 触发刷写并记录日志。
 *
 * 注意事项
 * - store_compressed_image() 的返回值类型为 storage_error_t（详见 image_storage.h）；
 * - 刷写前应确保 CPU1 已正确将“类型 + 负载”写入内部缓冲区（如 image_compress 等接口）。
 */

/**
 * @brief CPU0 主函数（负责触发刷写）
 * 流程：
 * 1. 初始化时钟、调试输出
 * 2. 等待多核同步（确保 CPU1 的外设初始化完成）
 * 3. 主循环：若 is_compress 为 true，则调用 store_compressed_image() 并复位标志
 */
int core0_main(void)
{
    clock_init();                   // 获取时钟频率<务必保留>
    debug_init();
    // 此处编写用户代码 例如外设初始化代码等
    // 此处编写用户代码 例如外设初始化代码等
    cpu_wait_event_ready();         // 等待所有核心初始化完毕
	while (TRUE)
	{
        if(is_compress)
        {
            uint8 ret = store_compressed_image();
            // 非 STORAGE_OK 视为失败；部分场景下 2 可能对应具体错误码
            if(ret != STORAGE_OK || ret == 2)
            {
                zf_log(0, "store_compressed_image failed!");
            }
            is_compress = false;
        }
	}
}

